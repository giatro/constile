<a id="cosa" name="cosa"></a> 
<h2>Cos'&egrave; .ConStile</h2>

<p>.ConStile &egrave; il sito italiano dedicato all'uso avanzato dei fogli di stile a cascata (cascade style sheets - CSS) per lo sviluppo di siti web leggeri, usabili, conformi agli standard <a href="http://www.w3.org/" title="World Wide Web Consortium">W3C</a>.</p>

<p>La missione di .ConStile &egrave; promuovere e diffondere la cultura del web creato secondo un concetto nuovo, il quale non interpreta gli standard come un limite alla creativit&agrave; del webmaster ma come la base per lo sviluppo di siti web usabili, non obsoleti, belli, fruibili da ogni tipo di utente (disabili o normodotati), con ogni dispositivo (PC, PDA, cellulari, ...).</p>

<a id="autore" name="w3c"></a> 
<h2>L'autore di .ConStile</h2> 

<img style="float: left;margin: 0 15px 0 0;position:relative" src="images/gtroiani.png" alt="[Foto: Gianluca Troiani, sullo sfondo un geco]" title="[Foto: Gianluca Troiani, sullo sfondo un geco]" />
<p>Nato a Roma nel Gennaio del '76, da alcuni anni cerco di conseguire una laurea in Ingegneria Elettronica (27 esami completati, ne restano 3).</p>
<p>Ho iniziato a realizzare siti web utilizzando una quantit&agrave; esagerata di immagini, tabelle ed &quot;effetti speciali&quot;: una orribile confusione. Abbandonati i tool <acronym title="[eng] What You See Is What You Get: Quello che vedi &egrave; quello che otterrai - in teoria">WYSIWYG</acronym>, ho abbracciato la causa dell'accessibilit&agrave;, dell'usabilit&agrave;, del rispetto degli standard <acronym title="World Wide Web Consortium">W3C</acronym>.</p>

<p>Da tempo mi diverto a sperimentare le potenzialit&agrave; dei CSS come unico strumento di definizione dell'aspetto delle pagine web. L'esperienza accumulata mi ha mostrato come i CSS siano (insieme all'XHTML) l'unica via di salvezza per i webmaster e gli utenti dei siti web, poich&eacute; l'utilizzo dei fogli di stile permette di realizzare siti web leggeri, accessibili, usabili, conformi agli standard W3C, facili da gestire, belli.</p>

<p>Ho cos&igrave; deciso di realizzare un intero sito web (quello che state visitando :) dedicato all'uso avanzato dei CSS, on-line dal 16 Ottobre 2002.</p>

<p>Attualmente collaboro con <a href="http://www.webaccessibile.org/" title="Webaccessibile.org - La risorsa in lingua italiana sull'accessibilit&agrave;" rel="esterno">Webaccessibile.org</a> e <a href="http://www.edmaster.it/goonline/" title="''GO!ONLINE Internet Magazine'' Mensile, Edizioni Master" rel="esterno">&quot;GO!ONLINE Internet Magazine&quot;</a>.</p>

