<a id="w3c" name="w3c"></a> 
<h2>.ConStile rispetta gli standard W3C</h2>

<p>.ConStile &egrave; scritto in <a href="http://validator.w3.org/check?uri=http://www.constile.org/" title="Controlla la conformit&agrave; di .ConStile" rel="esterno">XHTML 1.0 Strict valido</a> . Ci&ograve; permette agli utenti di visitare il sito con diversi dispositivi, come PDA e WebTV, senza incontrare alcun problema.</p>

<p>Il layout di .ConStile &egrave; realizzato completamente con <a href="http://jigsaw.w3.org/css-validator/validator?uri=http://www.constile.org/" title="Controlla la conformit&agrave; di .ConStile" rel="esterno">CSS 2.0 validi</a>. Questo permette la piena separazione fra i contenuti e il modo in cui essi sono presentati, permettendo la piena accessibilit&agrave; al sito a ogni tipo di utente. Browser pi&ugrave; datati (ad esempio Netscape Navigator 4.x) non sono in grado di interpretare correttamente i CSS usati da .ConStile (N.B: gli standard W3C sono stati stabiliti da diverso tempo, la non conformit&agrave; di browser come NN4.x &egrave; una lacuna del browser). Ci&ograve; significa che con alcuni browser l'impaginazione di .ConStile pu&ograve; apparire pi&ugrave; scarna, in nessun caso per&ograve; ci si trover&agrave; di fronte all&acute;impossibilit&agrave; di utilizzare il sito. La quasi totalit&agrave; dei siti oggi presenti nel web hanno un&acute;impaginazione basata sulle tabelle, ci&ograve; permette, con poco sforzo, di ottenere dei siti gradevoli da <strong>vedere</strong>, ma un&acute;utente non vedente che utilizzi un <span title="Un browser vocale per non vedenti legge i contenuti dei siti all'utente che pu&ograve; navigare nel sito solo attraverso ci&ograve; che il sintetizzatore vocal del browser gli dice" class="help">browser di tipo vocale</span> ovvero un utente normodotato che utilizzi dispositivi come PDA o <span title="Un browser testuale legge le pagine web ignorandone la grafica" class="help">browser di tipo testuale</span> possono trovarsi di fronte a un sito dai contenuti incomprensibili poich&eacute; grafica e contenuti non solo non sono separati ma addirittura sono fortemente mescolati. La scelta compiuta da .ConStile &egrave; quella di rinunciare alla grafica per alcuni browser pi&ugrave; datati, permettendo per&ograve; piena accessibilit&agrave; ai contenuti a tutti i possibili utenti, senza creare handicap per i disabili o per coloro che utilizzino dispositivi diversi da PC con browser in grado di interpretare le tabelle.</p>

<h3><a id="WAI">Accessibilit&agrave;</a></h3>
<p>.ConStile cerca di rispettare al massimo le linee guida <abbr title="Web Accessibility Initiative" class="help">WAI</abbr>, relative all'accessibilit&agrave;.</p>

<p>Sebbene i contenuti di .ConStile siano <strong>visibili con tutti i dispositivi</strong> per l'accesso a internet e <strong>con tutti i browser</strong> (il test &egrave; stato effettuato con IE4, IE5, IE6, NN4, NN6, Mozilla, Konqueror, Lynx, Mosaic), una grafica pi&ugrave; piacevole e raffinata e alcuni <strong>servizi aggiuntivi</strong> sono disponibili solo con i <strong>browser standard</strong>. Qualora voleste maggiori informazioni sui <strong>browser standard-compliant</strong> vi consigliamo di rivolgervi al sito del <a href="http://webstandards.org/act/campaign/buc/" title="Da questa pagina &egrave; possibile conoscere i browser standard **gratuiti** diponibili per ogni piattaforma" rel="esterno">Web Standard Project (WaSP)</a> dove verranno illustrati i <strong>browser standard gratuiti</strong> diponibili per ogni piattaforma.</p>

